public class Meal {
    private String mealName;
    private int MealInPlat;

    public Meal(String mealName, int mealInPlat) {
        this.mealName = mealName;
        this.MealInPlat = mealInPlat;
    }

    public String getMealName() {
        return mealName;
    }

    public int getMealInPlat() {
        return MealInPlat;
    }
}

